-- Piece metadata imported by IE_db.py from station dumps.

CREATE TABLE IF NOT EXISTS public.pieces (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_piece_id bigint,
    source_station text NOT NULL,
    side text NOT NULL,
    jsn text NOT NULL,
    created_at timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    decoat_length double precision,
    decoat_micrometer double precision,
    decoat_position double precision,
    decoat_speed real,
    decoat_trq double precision,
    endform_flag_length double precision,
    endform_flag_pos double precision,
    endform_hit1 double precision,
    endform_hit2 double precision,
    endform_hit3 double precision,
    endform_position_act double precision,
    endform_speed_act double precision,
    endform_trq_act double precision,
    CONSTRAINT pieces_unique_station_side_jsn UNIQUE (source_station, side, jsn)
);

ALTER TABLE public.model_results_central
    ADD COLUMN IF NOT EXISTS piece_id bigint;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'model_results_central_piece_id_fkey'
          AND conrelid = 'public.model_results_central'::regclass
    ) THEN
        ALTER TABLE public.model_results_central
            ADD CONSTRAINT model_results_central_piece_id_fkey
            FOREIGN KEY (piece_id) REFERENCES public.pieces(id) NOT VALID;
    END IF;
END $$;

CREATE INDEX IF NOT EXISTS pieces_station_side_jsn_idx
    ON public.pieces (source_station, side, jsn);

CREATE INDEX IF NOT EXISTS model_results_central_piece_id_idx
    ON public.model_results_central (piece_id);
