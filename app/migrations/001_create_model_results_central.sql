-- Central table used by the dashboard and by IE_db.py imports.

CREATE TABLE IF NOT EXISTS public.model_results_central (
    central_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_station text NOT NULL,
    source_id integer NOT NULL,
    img_name text NOT NULL,
    class_name text NOT NULL,
    confidence numeric(5,4) NOT NULL,
    created_at timestamp without time zone,
    jsn text GENERATED ALWAYS AS (split_part(img_name, '_Cam'::text, 1)) STORED,
    part_number text,
    CONSTRAINT model_results_central_unique_source UNIQUE (source_station, source_id)
);
